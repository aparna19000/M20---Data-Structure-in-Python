# Create a tuple with numbers
numbers = (10, 20, 30, 40, 50)

# Calculate the sum of all numbers in the tuple
total = sum(numbers)

# Find the length (number of elements) of the tuple
count = len(numbers)

# Calculate the average
average = total / count

# Display the results
print("Numbers in tuple:", numbers)
print("Total:", total)
print("Count:", count)
print("Average:", average)
